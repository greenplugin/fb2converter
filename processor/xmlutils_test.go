func TestTextFragment(t *testing.T) {

	for i, c := range casesTxtFragment {
		d := etree.NewDocument()
		if err := d.ReadFromString(c.in); err != nil {
			t.Fatal(err)
		}

		var res string
		for _, c := range d.Root().ChildElements() {
			if len(res) > 0 {
				res += "\n"
			}
			res += getFullTextFragment(c)
		}

		fmt.Printf("Case %d - FORMATTED:\n[%s]\n", i+1, res)
		if res != c.out {
			t.Fatalf("BAD RESULT\nEXPECTED:\n[%s]\nGOT:\n[%s]", spew.Sdump(c.out), spew.Sdump(res))
		}
	}
	t.Logf("OK - %s: %d cases", t.Name(), len(cases))
}

